package com.kekeinfo.web.admin.controller.orders;

import org.junit.Before;
import org.junit.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.web.FilterChainProxy;
import org.springframework.test.web.servlet.MvcResult;
import org.springframework.test.web.servlet.ResultActions;
import org.springframework.test.web.servlet.request.MockMvcRequestBuilders;
import org.springframework.test.web.servlet.result.MockMvcResultHandlers;
import org.springframework.test.web.servlet.result.MockMvcResultMatchers;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;

import com.kekeinfo.test.shop.controller.base.AbstractController;
import com.kekeinfo.test.shop.controller.base.SecurityRequestPostProcessors;

public class OrderControlerTest extends AbstractController {
	@Autowired
	private FilterChainProxy springSecurityFilterChain;

	@Override
	@Before
	public void setup() {
		this.mockMvc = MockMvcBuilders.webAppContextSetup(this.wac).addFilters(this.springSecurityFilterChain).build();
	}

	@Test
	public void test1() throws Exception {
		ResultActions ra = mockMvc.perform(MockMvcRequestBuilders.get("/admin/order/display.html")
				.with(SecurityRequestPostProcessors.userDeatilsService("admin")));
		ra.andDo(MockMvcResultHandlers.print());
		MvcResult mr = ra.andReturn();
		System.out.println(">>>>>testListContentPagesByAuth>>>>>" + mr.getResponse().getContentAsString());

	}

	@Test
	public void test2() throws Exception {
		ResultActions ra = mockMvc
				.perform(MockMvcRequestBuilders.get("/admin/orders/save.html")
						.with(SecurityRequestPostProcessors.userDeatilsService("admin")))
				.andExpect(MockMvcResultMatchers.model().attributeExists("order"))
				.andExpect(MockMvcResultMatchers.model().attributeExists("capturableTransaction"))
				.andExpect(MockMvcResultMatchers.model().attributeExists("capturableTransaction"))
				.andExpect(MockMvcResultMatchers.model().attributeExists("refundableTransaction"))
				.andExpect(MockMvcResultMatchers.model().attributeExists("order"))
				.andExpect(MockMvcResultMatchers.model().attributeExists("customer"))
				.andExpect(MockMvcResultMatchers.model().attributeExists("success"));
		ra.andDo(MockMvcResultHandlers.print());
		MvcResult mr = ra.andReturn();
		System.out.println(">>>>>testListContentPagesByAuth>>>>>" + mr.getResponse().getContentAsString());

	}
}